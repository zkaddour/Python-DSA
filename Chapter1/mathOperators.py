def ExponentOperator(a, b):
    # An exponent function that returns a to the power of b
    return a**b

def ModulusOperator(a, b):
    # A modulus function that returns the remainder of the division of a by b
    return a%b

def FloorDivisionOperator(a, b):
    # An integer division function that returns the floored result of a divided by b
    return a//b

def DivisionOperator(a, b):
    # A division function that returns the float result of a divided by b
    return a/b

def MultiplicationOperator(a, b):
    # A multiplication function that return a times b
    return a*b

def SubstractionOperator(a, b):
    # A substraction function that returns the difference between a and b
    return a-b

def AdditionOperator(a, b):
    # An addition function that returns the sum of a and b
    return a+b

